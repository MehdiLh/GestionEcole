package javaswingdev.form;

public class FormateurBean {
    private int IdFrt;
    private String NomFrt;
    private String PrenomFrt;

    public int getIdFrt() {
        return IdFrt;
    }

    public void setIdFrt(int IdFrt) {
        this.IdFrt = IdFrt;
    }

    public String getNomFrt() {
        return NomFrt;
    }

    public void setNomFrt(String NomFrt) {
        this.NomFrt = NomFrt;
    }

    public String getPrenomFrt() {
        return PrenomFrt;
    }

    public void setPrenomFrt(String PrenomFrt) {
        this.PrenomFrt = PrenomFrt;
    }
    
}
