    private void ModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifierActionPerformed
            
            StagiaireBean cb = new StagiaireBean();
    
            // Parse the integer value for IdStg
            int idStg;
            try {
                idStg = Integer.parseInt(IdStg.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Erreur : Veuillez entrer un entier pour Id.", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method if parsing fails
            }
            cb.setIdStg(idStg);

            cb.setNomStg(NomStg.getText());
            cb.setPrenomStg(PrenomStg.getText());
            cb.setFiliere(Filiere.getSelectedItem().toString());
            cb.setPromotion(Promotion.getText());
            
            ConnectDB con = ConnectDB.getcon(); 
            try {
                con.connect();
                con.modifierstg(cb); // Call the modifierstg method
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erreur lors de la modification du stagiaire : " + ex.getMessage(), "Erreur SQL", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Form_Stagiaire.class.getName()).log(Level.SEVERE, null, ex);
            }
            updateTableModel(); 
    }//GEN-LAST:event_ModifierActionPerformed

