    private void initializeTableModel() {
        tableModel = new DefaultTableModel();
        tableModel.addColumn("ID");
        tableModel.addColumn("Nom");
        tableModel.addColumn("Prenom");
        tableModel.addColumn("Filiere");
        tableModel.addColumn("Promotion");
        TableStg.setModel(tableModel);
    }

    private void updateTableModel() {
        tableModel.setRowCount(0); // Clear existing rows
        ConnectDB con = ConnectDB.getcon();
        con.connect();

        try {
            String sqlQuery = "SELECT * FROM Stagaire";
            ResultSet rs = con.executeQuery(sqlQuery);

            while (rs.next()) {
                Object[] rowData = {
                    rs.getInt("IdStg"),
                    rs.getString("NomStg"),
                    rs.getString("PrenomStg"),
                    rs.getString("Filiere"),
                    rs.getString("Promotion")
                };
                tableModel.addRow(rowData);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Form_Stagiaire.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void addTableClickListener() {
        TableStg.getSelectionModel().addListSelectionListener((ListSelectionEvent e) -> {
            if (!e.getValueIsAdjusting()) {
                int selectedRow = TableStg.getSelectedRow();
                if (selectedRow != -1) { // Ensure a row is selected
                    Object idStgObj = TableStg.getValueAt(selectedRow, 0);
                    Object nomStgObj = TableStg.getValueAt(selectedRow, 1);
                    Object prenomStgObj = TableStg.getValueAt(selectedRow, 2);
                    Object filiereObj = TableStg.getValueAt(selectedRow, 3);
                    Object promotionObj = TableStg.getValueAt(selectedRow, 4);
                    
                    // Check for null values before converting to string
                    String idStg = idStgObj != null ? idStgObj.toString() : "";
                    String nomStg = nomStgObj != null ? nomStgObj.toString() : "";
                    String prenomStg = prenomStgObj != null ? prenomStgObj.toString() : "";
                    String filiere = filiereObj != null ? filiereObj.toString() : "";
                    String promotion = promotionObj != null ? promotionObj.toString() : "";
                    
                    IdStg.setText(idStg);
                    NomStg.setText(nomStg);
                    PrenomStg.setText(prenomStg);
                    Filiere.setSelectedItem(filiere);
                    Promotion.setText(promotion);
                }
            }
        });
    }
    
